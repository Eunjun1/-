// Property를 이용한 방법
class Calc01{
  //방법은 상관없다.
  
  //Property(Attribute, Field도 같은말)
  int num1 = 0;
  int num2 = 0;

  //Constructor(생성자)  
  //안적을시 기본생성자 생성


  //Method
  int addition(){
    return num1 + num2;
  }

  int subtraction(){
    return num1 - num2;
  }
  int multiplication(){
    return num1 * num2;
  }

  double division(){
    return num1 / num2;
  }

}  //Calc01